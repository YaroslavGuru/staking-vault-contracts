import { Address } from "viem";

export interface ContractAddresses {
  Yaroslav: Address;
  rYaroslav: Address;
  YaroslavStakingVault: Address;
}

export function getContractAddresses(): ContractAddresses {
  const addressesJson = process.env.NEXT_PUBLIC_CONTRACT_ADDRESSES || "{}";
  try {
    return JSON.parse(addressesJson) as ContractAddresses;
  } catch (error) {
    console.error("Failed to parse contract addresses:", error);
    return {
      Yaroslav: "0x0000000000000000000000000000000000000000" as Address,
      rYaroslav: "0x0000000000000000000000000000000000000000" as Address,
      YaroslavStakingVault: "0x0000000000000000000000000000000000000000" as Address,
    };
  }
}

